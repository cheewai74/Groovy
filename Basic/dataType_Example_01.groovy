class dataType_example_01{

    static void main(String[] args){

        int x = 5;
        long y = 100L;
        float a = 10.56f;
        double b = 10.5e40;
        BigInteger bi = 30g;
        BigDecimal bd = 3.5g;

        println("x: " + x + " y: " + y + " a: " + a + " bi: " + bi + " bd: " + bd);
    }
}